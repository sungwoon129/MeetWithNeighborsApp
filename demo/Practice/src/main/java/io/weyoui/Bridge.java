package io.weyoui;

public class Bridge {

    private int firstIsland;
    private int secondIsland;
    private int cost;

    public Bridge() {
    }

    public int getFirstIsland() {
        return firstIsland;
    }

    public void setFirstIsland(int firstIsland) {
        this.firstIsland = firstIsland;
    }

    public int getSecondIsland() {
        return secondIsland;
    }

    public void setSecondIsland(int secondIsland) {
        this.secondIsland = secondIsland;
    }

    public int getCost() {
        return cost;
    }

    public void setCost(int cost) {
        this.cost = cost;
    }
}
